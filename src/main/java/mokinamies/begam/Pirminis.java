package mokinamies.begam;

import java.util.List;

public class Pirminis {
	
	private List<Begikai> begikai;
	private List<Trasos> trasos;
	/**
	 * @return the begikai
	 */
	public List<Begikai> getBegikai() {
		return begikai;
	}
	/**
	 * @param begikai the begikai to set
	 */
	public void setBegikai(List<Begikai> begikai) {
		this.begikai = begikai;
	}
	/**
	 * @return the trasos
	 */
	public List<Trasos> getTrasos() {
		return trasos;
	}
	/**
	 * @param trasos the trasos to set
	 */
	public void setTrasos(List<Trasos> trasos) {
		this.trasos = trasos;
	}
	
	
}